// JavaScript Math ceil()
//--------------------------
const num=Math.ceil(9.5)
// console.log(num);  // 10,rounds a decimal number up to the next largest integer and returns it.That is, 9.5 will be rounded to 10 (next largest integer).

// JavaScript Math floor()
//-----------------------------
const num1=8.6;
const num11=Math.floor(num1)
console.log(num11);  // 8 rounds down a number to the next smallest integer.

// JavaScript Math round()
//---------------------------
const num2=Math.round(55.5) //56
const num22=Math.round(10.3) //10
console.log(`${num2}  ${num22}`)  // 56  10 returns the number rounded to the nearest integer

// JavaScript Math trunc()
//--------------------------
const num3=Math.trunc(650.67)
const num33=Math.trunc(-45.98)
console.log(num33)  //-45
console.log(num3) // 650 The trunc() method just removes the digits after the decimal point and returns the integer portion.


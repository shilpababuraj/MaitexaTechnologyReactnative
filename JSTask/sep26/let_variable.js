
// primitive data types with let variable


let number = 10,  //number
 name ="shilpa",  //string
 boolean =true;    //boolean

 let base=null;
 console.log(base);  //null

 let base1=0;
 console.log(base1);  //0

let base2 =undefined;   

 console.log(base2) // undefined

 console.log(typeof base);  // object
 console.log(typeof base2); //undefined
 console.log(typeof number) //number
 console.log(typeof name) //string
 console.log(typeof boolean) //boolean
 console.log(number); //10
 console.log(name); // shilpa
 console.log(boolean); //true
 console.log(`number:${number} name:${name} boolean:${boolean}`) //number:10 name:shilpa boolean:true










